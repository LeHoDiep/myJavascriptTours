// Design Pattern: là khuôn mẫu thiết kế có sẳn để giải quyết 1 vấn đề
//                 giúp code trông thống nhất và hiệu quả
//                  thường thì mình không làm ra nó vì nó có tính vĩ mô cao
//                      ta thường sử dụng của các tiền bối đi trước

//ví dụ: mình có 1 app to-do-list đơn giản khoản 200 dòng
//          chỉ cần dùng các function gọi đi gọi lại là đủ rồi
//      nhưng có 2-3 người vào code cùng nhau
//      mỗi người code 1 phong cách khác nhau
//      dẫn đến code dần dần mất đi sự nhất quán
//      mặc dù code chạy được, nhưng đọc lại rất khó bảo trì
//      chưa kể đến việc: ban đầu thiết kế code không khoa học
//          sẽ rất khó để triễn khai phát triễn mở rộng
//=>đó là lý do cần thống nhất design pattern ngay từ đầu
//Design pattern có ở mọi loại ngôn ngữ
// mỗi một ngôn ngữ lại có nhiều loại design pattern khác nhau
// với Frontend mình hay dùng các frame như React, Angula, Vue
//      mỗi thằng lại có một cách code khác nhau, đó là do chúng áp dụng
//                                          các design pattern khác nhau
//  Angular sử dụng : depedency, Observer
//  React   sử dụng : High Order Component, cũng được biết đến như là Decorator Pattern

